class Solution:
    def numIslands(self, grid):
        """
        :type grid: List[List[str]]
        :rtype: int
        """
        offsets = [(1,0), (0,1), (-1,0), (0,-1)]
        islandCount = 0
        toExplore = []

        for y in range(len(grid)):
            for x in range(len(grid[0])):
                if grid[y][x] == "1":
                    islandCount += 1
                    toExplore.append((y, x))

                    while toExplore:
                        ey, ex = toExplore.pop()
                        if grid[ey][ex] == '1':
                            grid[ey][ex] = '#'

                            for dy, dx in offsets:
                                ny = ey + dy
                                nx = ex + dx
                                if nx < 0 or ny < 0 or ny >= len(grid) or nx >= len(grid[0]):
                                    pass
                                elif grid[ny][nx] == '1':
                                    toExplore.append((ny, nx))
        return islandCount



